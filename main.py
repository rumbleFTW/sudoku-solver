def isSafe(board, index, value):
    column = index % 9
    columnlist = []
    rowlist = []
    row = index // 9

    box1 = [board[0], board[1], board[2], board[9], board[10], board[11], board[18], board[19], board[20]]
    box2 = [board[3], board[4], board[5], board[12], board[13], board[14], board[21], board[22], board[23]]
    box3 = [board[6], board[7], board[8], board[15], board[16], board[17], board[24], board[25], board[26]]
    box4 = [board[27], board[28], board[29], board[36], board[37], board[38], board[45], board[46], board[47]]
    box5 = [board[30], board[31], board[32], board[39], board[40], board[41], board[48], board[49], board[50]]
    box6 = [board[33], board[34], board[35], board[42], board[43], board[44], board[51], board[52], board[53]]
    box7 = [board[54], board[55], board[56], board[63], board[64], board[65], board[72], board[73], board[74]]
    box8 = [board[57], board[59], board[60], board[66], board[67], board[68], board[75], board[76], board[77]]
    box9 = [board[60], board[61], board[62], board[69], board[70], board[71], board[78], board[79], board[80]]

    if row == 0:
        rowlist = board[0:9]
    elif row == 1:
        rowlist = board[9:18]
    elif row == 2:
        rowlist = board[18:27]
    elif row == 3:
        rowlist = board[27:36]
    elif row == 4:
        rowlist = board[36:45]
    elif row == 5:
        rowlist = board[45:54]
    elif row == 6:
        rowlist = board[54:63]
    elif row == 7:
        rowlist = board[63:72]
    elif row == 8:
        rowlist = board[71:81]

    if (0<=index%9<=2 and 0<=index//9<=2):
        box = box1
    if (3<=index%9<=5 and 0<=index//9<=2):
        box = box2
    if (6<=index%9<=8 and 0<=index//9<=2):
        box = box3
    if (0<=index%9<=2 and 3<=index//9<=5):
        box = box4
    if (3<=index%9<=5 and 3<=index//9<=5):
        box = box5
    if (6<=index%9<=8 and 3<=index//9<=5):
        box = box6
    if (0<=index%9<=2 and 6<=index//9<=8):
        box = box7
    if (3<=index%9<=5 and 6<=index//9<=8):
        box = box8
    if (6<=index%9<=8 and 6<=index//9<=8):
        box = box9
    for i in range(0, 9):
        columnlist.append(board[column+(9)*i])
    if (value in rowlist or value in columnlist or value in box):
        return False
    else:
        return True



def find(board):
    for i in range(0, 81):
        if board[i] == '':
            return i



def solve(board):
    index = find(board)
    if index == None:
        return True
    else:
        ind = index
        for j in range(1, 10):
            if isSafe(board, ind, j):
                board[ind] = j
                if solve(board):
                    return board
                board[ind] = ''



